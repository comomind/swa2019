package cmu.voip.controller.http.account.vo;

public class UserNetworkInfoVO {
	String email;
	String name;
	String phonenum;
	String ip;
	int port;
	int status;
	String token;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPhonenum() {
		return phonenum;
	}
	public void setPhonenum(String phonenum) {
		this.phonenum = phonenum;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	@Override
	public String toString() {
		return "UserNetworkInfoVO [email=" + email + ", name=" + name + ", phonenum=" + phonenum + ", ip=" + ip
				+ ", port=" + port + ", status=" + status + ", token=" + token + "]";
	}
}
